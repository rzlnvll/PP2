# Functions can return values using the return statement:
def summ(x, y):
    return x + y
result = summ(5, 3)
print(result)

#Functions can return any data type, including lists, tuples, dictionaries, and more.
def fruit():
    return ["apple", "banana", "cherry"]
fruits = fruit()
print(fruits[0])
print(fruits[1])
print(fruits[2])